def create_room(matrix, x, y, w, h):
    """Заливка прямоугольной комнаты в матрице (стены остаются 0, внутри 1)"""
    for i in range(y, y + h):
        for j in range(x, x + w):
            if 0 <= i < len(matrix) and 0 <= j < len(matrix[0]):
                matrix[i][j] = 1


def create_corridor(matrix, x1, y1, x2, y2):
    """L-образный коридор между центрами комнат"""
    # Горизонтальная часть
    for x in range(min(x1, x2), max(x1, x2) + 1):
        if 0 <= y1 < len(matrix) and 0 <= x < len(matrix[0]):
            matrix[y1][x] = 1
    # Вертикальная часть
    for y in range(min(y1, y2), max(y1, y2) + 1):
        if 0 <= y < len(matrix) and 0 <= x2 < len(matrix[0]):
            matrix[y][x2] = 1


def rooms_overlap(x1, y1, w1, h1, x2, y2, w2, h2, padding=1):
    """Проверка пересечения с отступом"""
    return not (x1 + w1 + padding <= x2 or x2 + w2 + padding <= x1 or
                y1 + h1 + padding <= y2 or y2 + h2 + padding <= y1)


def generate_dungeon(width=45, height=20, rooms_count=8):
    matrix = [[0 for _ in range(width)] for _ in range(height)]
    rooms = []

    min_room_size = 4
    max_room_size = 8

    for _ in range(rooms_count * 3):  # Пытаемся с запасом
        if len(rooms) >= rooms_count:
            break

        w = random.randint(min_room_size, max_room_size)
        h = random.randint(min_room_size, max_room_size)
        x = random.randint(1, width - w - 1)
        y = random.randint(1, height - h - 1)

        # Проверка на пересечение с существующими комнатами
        overlap = False
        for (rx, ry, rw, rh) in rooms:
            if rooms_overlap(x, y, w, h, rx, ry, rw, rh):
                overlap = True
                break

        if not overlap:
            rooms.append((x, y, w, h))
            create_room(matrix, x, y, w, h)

    # Соединяем комнаты коридорами (по порядку создания)
    for i in range(1, len(rooms)):
        x1, y1, w1, h1 = rooms[i - 1]
        x2, y2, w2, h2 = rooms[i]
        # Центры комнат
        cx1, cy1 = x1 + w1 // 2, y1 + h1 // 2
        cx2, cy2 = x2 + w2 // 2, y2 + h2 // 2
        create_corridor(matrix, cx1, cy1, cx2, cy2)

    # Старт в центре первой комнаты
    if rooms:
        start_x = rooms[0][0] + rooms[0][2] // 2
        start_y = rooms[0][1] + rooms[0][3] // 2
    else:
        start_x, start_y = 1, 1  # На случай, если комнат нет

    return matrix, start_x, start_y, rooms # добавлен возврат rooms



