import pygame
from dungeon_and_dragons.graphics.colors import COLORS
from dungeon_and_dragons.models.entity import Entity

class Renderer:
    def __init__(self, screen, game_state):
        self.screen = screen
        self.state = game_state
        self.tile_size = 20  # Размер одного тайла в пикселях

    def render(self):
        self.screen.fill((0, 0, 0))  # Заливка экрана черным фоном

        # 1. Рисуем карту
        dungeon = self.state.dungeon
        if dungeon:
            y = 0
            for i in dungeon.matrix:
                x = 0
                for tile in i:
                    if tile == 1:
                        color = COLORS['floor']
                    else:
                        color = COLORS['wall']
                    pygame.draw.rect(self.screen, color, [x * self.tile_size, y * self.tile_size, self.tile_size, self.tile_size])
                    x += 1
                y += 1





        # 2. Рисуем монстров
        for monster in self.state.monsters:
            if monster.is_alive:
                x = monster.x * self.tile_size + self.tile_size // 2
                y = monster.y * self.tile_size + self.tile_size // 2
                radius = self.tile_size // 2
                pygame.draw.circle(self.screen, COLORS['monster'], [x, y], radius)






        # 3. Рисуем игрока
        if self.state.player:
            # узнаем координаты x и y игрока и рисуем круг на его месте
            player = self.state.player
            x = player.x * self.tile_size + self.tile_size // 2
            y = player.y * self.tile_size + self.tile_size // 2
            radius = self.tile_size // 2
            pygame.draw.circle(self.screen, COLORS['player'], [x, y], radius)



        # 4. Панель статов (простой текст)
        font = pygame.font.SysFont(None, 24)
        if self.state.player:
            stats_text = f"HP: {player.hp}, {player.max_hp}, ATTK: {player.attack_power}, DEF: {player.defense}"# здесь в переменную stats_text нужно сохранить единую строку, в которой будет хп персонажа (текущее и максимальное), атака и защита
            surface = font.render(stats_text, True, (255, 255, 255))
            self.screen.blit(surface, (10, 10))



        # 5. Лог (последние 3 сообщения)
        if self.state.message_log:
            log_font = pygame.font.SysFont(None, 20)
            messages = self.state.message_log[-3:]
            for i, msg in enumerate(messages):
                surf = log_font.render(msg, True, (200, 200, 200))
                self.screen.blit(surf, (10, self.screen.get_height() - 60 + i * 20))


